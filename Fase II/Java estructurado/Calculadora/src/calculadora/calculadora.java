    private void btnSqrtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSqrtActionPerformed
        igual = 6;
        
        if (op == false) {
            memoria1 = Double.parseDouble(txtAbajo.getText());
            op = true;
        } else {
            memoria2 = Double.parseDouble(txtAbajo.getText());
            memoria1 = Math.sqrt(memoria1=memoria2);
            txtArriba.setText("" + memoria1);
        }
        
        limpiarCompleto();
    }//GEN-LAST:event_btnSqrtActionPerformed

