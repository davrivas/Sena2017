    private void btnDividedByActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDividedByActionPerformed
        igual = 4;

        if (op == false) {
            memoria1 = Double.parseDouble(txtAbajo.getText());
            op = true;
            txtArriba.setText("" + memoria1 + " / ");
        } else {
            memoria2 = Double.parseDouble(txtAbajo.getText());

            if (memoria2 == 0) {
                txtArriba.setText("Error, división por 0");
            } else {
                memoria1 = memoria1 / memoria2;
                txtArriba.setText("" + memoria1 + " / ");
            }
        }

        limpiarCompleto();
    }//GEN-LAST:event_btnDividedByActionPerformed

    private void btnPercentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPercentActionPerformed
        igual = 5;
        memoria2 = Double.parseDouble(txtAbajo.getText());

        if (op == false) {
            memoria1 = Double.parseDouble(txtAbajo.getText());
            op = true;
        } else {
            memoria1 = (memoria1 * memoria2) / 100;
            txtArriba.setText("" + memoria1 + " % ");
        }

        limpiarCompleto();
    }//GEN-LAST:event_btnPercentActionPerformed

