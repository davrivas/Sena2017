    private void btnEqualsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEqualsActionPerformed
        if (op == true) {
            memoria2 = Double.parseDouble(txtAbajo.getText());

            switch (igual) {
                case 1:
                    memoria1 = memoria1 + memoria2;
                    txtArriba.setText("" + memoria1 + " + ");
                    limpiar();
                    break;

                case 2:
                    memoria1 = memoria1 - memoria2;
                    txtArriba.setText("" + memoria1 + " - ");
                    limpiar();
                    break;

                case 3:
                    memoria1 = memoria1 * memoria2;
                    txtArriba.setText("" + memoria1 + " * ");
                    limpiar();
                    break;

                case 4:
                    if (memoria2 == 0) {
                        txtArriba.setText("Error: división por 0");
                    } else {
                        memoria1 = memoria1 / memoria2;
                        txtArriba.setText("" + memoria1 + " / ");
                    }

                    limpiar();
                    break;

                case 5:
                    memoria1 = (memoria1 * memoria2) / 100;
                    txtArriba.setText("" + memoria1);

                    limpiar();
                    break;

                case 6:
                    memoria1 = Math.sqrt(memoria1 = memoria2);
                    txtArriba.setText("" + memoria1);
                    
                    limpiar();
                    break;

                case 7:
                    if (memoria2 == 0) {
                        txtArriba.setText("Error: división por 0");
                    } else {
                        memoria1 = 1 / memoria2;
                        txtArriba.setText("" + memoria1);
                    }
                    
                    limpiar();
                    break;
            }
        }
    }//GEN-LAST:event_btnEqualsActionPerformed

